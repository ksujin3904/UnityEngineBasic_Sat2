// 변수를 사용하겠다고 '선언' 하는 것의 의미
//  => 특정 형태의 값이 들어갈 수 있는(쓸 수 있는) 메모리영역의 공간을 할당

// 변수 선언 형태
// 자료형 이름;

// 초기화 형태
// 자료형 이름 = 초기화값;

int number = 25; // 부호가 있는 4byte 정수형
short shortnumber; // 부호가 있는 2byte 정수형
long longnumber; // 부호가 있는 8byte 정수형
float floatnumber = 0.0f; // 4byte 실수형
double doublenumber = 0.0; // 8byte 실수형

char c = 'k'; // 2byte 문자형
string s = "안녕"; // 2byte * 문자형 + 1byte (null) 문자열형
bool isTrue = false; // 1byte 논리형
